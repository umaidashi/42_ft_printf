#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*res;
	t_list	*new;

	if (!lst)
		return (0);
	res = ft_lstnew(f(lst->content));
	if (!res)
		return (0);
	while (lst->next)
	{
		new = ft_lstnew(f(lst->next->content));
		if (!new)
		{
			ft_lstclear(&res, del);
			return (0);
		}
		ft_lstadd_back(&res, new);
		lst = lst->next;
	}
	return (res);
}
